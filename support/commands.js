import login from "../page_object/LoginPage";

Cypress.Commands.add('Login_in_site',(email,password) => {
    const ln1=new login();
        ln1.openpage();
        ln1.setusename(email);
        ln1.setpassword(password);
        ln1.clicksubmit();
        ln1.verifylogin();
})